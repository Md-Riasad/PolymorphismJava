
public class Calculation {

	
	public int add (int x, int y) {
		int z =x+y;
		
		System.out.println("Integer Result :"+z);
		
		return z;
	}
	
	public int add (int x, int y ,int z) {
		int k =x+y+z;
		
		System.out.println("Integer Result :"+k);
		
		return k;
	}

	public double add (double x, int y) {
		double z =x+y;
		
		System.out.println("Integer Result :"+z);
		
		return z;
	}
}
